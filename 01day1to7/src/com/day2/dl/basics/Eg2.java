package com.day2.dl.basics;

public class Eg2 {
	// Data Types
	// Number
	static byte b;
	static short s;
	static int i;
	static long l;
	// Floating points
	static float f;
	static double d;
	// Character
	static char ch;
	// Boolean
	static boolean bb;

	public static void main(String[] args) {
		System.out.println(b + " " + s + " " + i + " " + l);
		System.out.println(f + " " + d);
		System.out.println(ch);
		System.out.println(bb);

		System.out.println(b + ":" + s);
		System.out.println(bb + "?" + f);
	}

}
