package com.day9.poly.operatoroverloading;

public class Eg1 {
	//operator overloading
	
	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		
		String s1 = "A";
		String s2 = "B";
		
		System.out.println("value of a : " + a + "value of b : " + b);
		System.out.println("value of s1 : " + s1 + "value of s2 : " + s2);
	}

}
